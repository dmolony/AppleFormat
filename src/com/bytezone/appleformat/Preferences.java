package com.bytezone.appleformat;

// -----------------------------------------------------------------------------------//
public abstract class Preferences
// -----------------------------------------------------------------------------------//
{
  protected String name;

  // ---------------------------------------------------------------------------------//
  public Preferences (String name)
  // ---------------------------------------------------------------------------------//
  {
    this.name = name;
  }

  // ---------------------------------------------------------------------------------//
  public String getName ()
  // ---------------------------------------------------------------------------------//
  {
    return name;
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return String.format ("Name .................. %s%n", name);
  }
}
